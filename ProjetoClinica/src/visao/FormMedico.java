    public void readJTableMedicos(){
        DefaultTableModel modelo = (DefaultTableModel) jTableMedicos.getModel();
        
        modelo.setNumRows(0);
        
        MedicoDAO mdao = new MedicoDAO();
        
        for(BeansMedico m: mdao.read()){
            
            modelo.addRow(new Object[]{
               m.getCodigo(),
               m.getCRM(),
               m.getNome(),
               m.getEspecialidade()
               
            });
        }
    }
    public void readJTableByNome(String pesquisa){
        DefaultTableModel modelo = (DefaultTableModel) jTableMedicos.getModel();
        
        modelo.setNumRows(0);
        
        MedicoDAO mdao = new MedicoDAO();
        
        for(BeansMedico m: mdao.readByNome(pesquisa)){
            
            modelo.addRow(new Object[]{
               m.getCodigo(),
               m.getCRM(),
               m.getNome(),
               m.getEspecialidade()
               
            });
        }
    }
