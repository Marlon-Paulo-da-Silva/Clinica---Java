    private void jButtonExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonExcluirActionPerformed
       BeansMedico m = new BeansMedico();
       MedicoDAO dao = new MedicoDAO();
        
       if(jTableMedicos.getSelectedRow() != -1){
       
           m.setCodigo(Integer.parseInt(jTextFieldCodigo.getText()));
       
        dao.delete(m);
        jTextFieldNome.setText("");
        jFormattedTextFieldCRM.setText("");
        
        jTextFieldNome.setEnabled(false);
        jFormattedTextFieldCRM.setEnabled(false);
        jComboBoxEspecialista.setEnabled(false);
        jButtonSalvar.setEnabled(false);
        
       readJTableMedicos();
        }else{
            JOptionPane.showMessageDialog(null, "clique em algum cadastro");
            
        }
    
        
    }//GEN-LAST:event_jButtonExcluirActionPerformed
        
