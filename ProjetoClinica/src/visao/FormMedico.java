    private void jButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarActionPerformed
       BeansMedico m = new BeansMedico();
       MedicoDAO dao = new MedicoDAO();
       if(jTextFieldNome.getText().isEmpty()){
           jLabel7.setVisible(true);
           jTextFieldNome.requestFocus();
           
           
           
           
       }else if(jFormattedTextFieldCRM.getText().isEmpty()){
           jLabel8.setVisible(true);
           jFormattedTextFieldCRM.requestFocus();
       }else{
           //jLabel7.setVisible(false);
           //jLabel8.setVisible(false);
       
        if(flag == 1){
       m.setNome(jTextFieldNome.getText());
       m.setCRM(Integer.parseInt(jFormattedTextFieldCRM.getText()));
       
       BeansEspecialidades espe = (BeansEspecialidades) jComboBoxEspecialista.getSelectedItem();
       m.setEspecialidade(espe.getEspecilizacao());
       
       dao.create(m);
       
       jTextFieldNome.setText("");
       jFormattedTextFieldCRM.setText("");
       jTextFieldPesquisa.setText("");
        
       jTextFieldNome.setEnabled(false);
       jFormattedTextFieldCRM.setEnabled(false);
       jComboBoxEspecialista.setEnabled(false);
       jButtonSalvar.setEnabled(false);
       jButtonCancelar.setEnabled(false);
       jButtonExcluir.setEnabled(false);
        
        
       readJTableMedicos();
        }else{
            try {
                
            
        m.setNome(jTextFieldNome.getText());
        m.setCRM(Integer.parseInt(jFormattedTextFieldCRM.getText()));
        
        //BeansEspecialidades espe = (BeansEspecialidades) jComboBoxEspecialista.getSelectedItem();
       // m.setEspecialidade(espe.getEspecilizacao());
        m.setEspecialidade(jComboBoxEspecialista.getSelectedItem().toString());
        m.setCodigo(Integer.parseInt(jTextFieldCodigo.getText()));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "erro antes de editar  "+e);
            }
        dao.editar(m);
        
        jTextFieldNome.setText("");
        jFormattedTextFieldCRM.setText("");
        jTextFieldPesquisa.setText("");
        
        jTextFieldNome.setEnabled(false);
        jFormattedTextFieldCRM.setEnabled(false);
        jComboBoxEspecialista.setEnabled(false);
        jButtonSalvar.setEnabled(false);
        jButtonCancelar.setEnabled(false);
        jButtonExcluir.setEnabled(false);
        jButtonNovo.setEnabled(true);
        
        readJTableMedicos();
        
        }
       }
    }//GEN-LAST:event_jButtonSalvarActionPerformed

